package Corejavaelements;

public class Reserved_keywords {                        //classname should start with uppercase

	public static void main(String[] args) {           // methodname must starts with smallletter and in verb
		int element1 = 10;                             //  element1 is an identifier 
		int element2=20;
		
		int sum = element1+element2;
		System.out.println("sum is"+sum);
		System.out.println("java is a freeform language");
		
		boolean ispassed = true;                         //true,false=keywords
		boolean isfail = false;
		
		System.out.println("x is passed: " +ispassed); //reserved keywords are 61 two of them are true,false
		System.out.println("x is passed: " +isfail);   //unused=goto,label
		

	}

}
